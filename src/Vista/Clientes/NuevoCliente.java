    public void consultaDNI(String dni) {
        String conn = "https://dniruc.apisperu.com/api/v1/dni/" + dni + "?token=eyJ0eXAiOiJKV1QiLCJhbGciOiJIUzI1NiJ9.eyJlbWFpbCI6IndpbGxpYW1fbW9ucm95Nzc3QGhvdG1haWwuY29tIn0.naYZNJRoxT6XWBPGsAbq06isggggIxzjkXgMAormgMk";

        try {
            URL url = new URL(conn);
            URLConnection request = url.openConnection();
            request.connect();

            JsonParser jp = new JsonParser();
            JsonElement root = jp.parse(new InputStreamReader((InputStream) request.getContent()));
            if (root.isJsonObject()) {
                JsonObject rootobj = root.getAsJsonObject();
                String ndni = rootobj.get("dni").getAsString();
                String nom = rootobj.get("nombres").getAsString();                
                String ap = rootobj.get("apellidoPaterno").getAsString();
                String am = rootobj.get("apellidoMaterno").getAsString();
                txtNombre.setText(iniMayus(nom));
                txtApellido.setText(iniMayus(ap)+" "+iniMayus(am));
                txtDNI.setText(ndni);
                txtDireccion.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "DNI no v√°lido");
            System.out.println("Error: " + e.getMessage());
        }
    }

    public String iniMayus(String cadena) {
        cadena = cadena.toLowerCase();
        char[] caracteres = cadena.toCharArray();
        caracteres[0] = Character.toUpperCase(caracteres[0]);
        // el -2 es para evitar una excepci√≥n al caernos del arreglo
        for (int i = 0; i < cadena.length() - 2; i++) // Es 'palabra'
        {
            if (caracteres[i] == ' ' || caracteres[i] == '.' || caracteres[i] == ',') // Reemplazamos
            {
                caracteres[i + 1] = Character.toUpperCase(caracteres[i + 1]);
            }
        }
        return new String(caracteres);
    }
    
    public void agregarCliente(){
        
        String sql = "insert into cliente (nombres,apellidos,dni,direccion) values (?,?,?,?)";
        
        try{
            PreparedStatement pst = con.prepareStatement(sql);
            
            pst.setString(1, txtNombre.getText());
            pst.setString(2, txtApellido.getText());
            pst.setInt(3, Integer.parseInt(txtDNI.getText()));
            pst.setString(4, txtDireccion.getText());
            
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Cliente Agregado Exitosamente");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
    
