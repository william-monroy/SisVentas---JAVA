    public void mostrarDatos() {
        String[] titulos = {"Codigo", "Nombre", "Unidad"};
        String[] registros = new String[3];

        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        String SQL = "select * from categoria";

        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);

            while (rs.next()) {
                registros[0] = rs.getString("cod_categoria");
                registros[1] = rs.getString("nombre");
                registros[2] = rs.getString("unidad");

                modelo.addRow(registros);
            }

            tabla.setModel(modelo);
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Mostrar Datos: " + e.toString());
        }
    }
    
    public void agregarProducto(){
        
        String sql = "insert into producto (cod_categoria,nombre,precio,stock) values (?,?,?,?)";
        
        try{
            PreparedStatement pst = con.prepareStatement(sql);
            
            int filaSeleccionada = tabla.getSelectedRow();
            String txtfilaSeleccionada = (String) tabla.getValueAt(filaSeleccionada, 0);
            
            pst.setInt(1, Integer.parseInt(txtfilaSeleccionada));
            pst.setString(2, txtNombre.getText());
            pst.setDouble(3, Double.parseDouble(txtPrecio.getText().replace(",", "")));
            pst.setDouble(4, Double.parseDouble(txtStock.getText().replace(",", "")));
            
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Producto Agregado Exitosamente");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

